<h3 class="left" style="text-align: justify;">Store Staff</h3>
<p style="text-align: justify;">Wowrol store staff feature gives you  opportunity to administrate your store through multiple staff members. The main benefit of this feature to store owner is that they don't need to share store login password with staff. store owner can add maximum 5 store staff to their store. Each store staff can control categories ,products,shipping, respond to orders but they can not access home page,profile page.</p>

</div>
<div class="block " style="text-align: justify;">Store owner can control store staff account by store staff hash . store staff hash is 15 digit alphanumeric key. By default a key is automatically generated.   Store owner can easily change this key from your store setting.</div>
<p style="text-align: justify;"></p>
<p style="text-align: justify;"><strong>Appointing store staff </strong></p>
<p class="block " style="text-align: justify;">To appoint a staff member to your store. you have to tell them 3 parmenter</p>

<ol style="text-align: justify;">
	<li> Store staff hash</li>
	<li> Staff Username</li>
	<li>Staff password</li>
</ol>
<p class="block " style="text-align: justify;">with these three credential your appointed store staff can easily access your store form <a href="/storestafflogin">http://wowrol.com/hstorestafflogin</a> .</p>
<p style="text-align: justify;"><strong> Managing store staff </strong></p>
<p class="block " style="text-align: justify;">To Manage  store staff go to  <a href="/storestafflogin">http://wowrol.com/storestaff </a>. At this page you can easily create ,edit store staff  account.</p>
<p style="text-align: justify;"><strong>Take control from all store staff</strong></p>
<p class="block " style="text-align: justify;">To take control from store staff you have to change store staff hash and all store staff member  will logout from your store. or you can change username password of particular store staff.</p>
